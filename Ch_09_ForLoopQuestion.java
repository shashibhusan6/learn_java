        int largest = -1;
        int secondLargest = -1;

        for (; num > 0; num /= 10) {

            int digit = num % 10;

            if (digit > largest) {
                secondLargest = largest;
                largest = digit;
            } else if (digit > secondLargest && digit != largest) {
                secondLargest = digit;
            }

        }

